    public void recargar() {
        productos = userSessionBeanLocal.getProductos(null);
        clientes = userSessionBeanLocal.getClientes(null);
        listFieldClientes.setListData(userSessionBeanLocal.listaLabelsClientes(null));
        listFieldProductos.setListData(userSessionBeanLocal.listaLabelsProductos(null));
        factura = new Factura(new Date(), clienteSelected);
        relFacturaProductos.clear();
        listFieldProductosSeleccionados.setListData(
                userSessionBeanLocal.listaLabelsRelFacturaProducto(relFacturaProductos));
    }

