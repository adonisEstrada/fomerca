    private void buttonAgregarProductoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonAgregarProductoActionPerformed
        if (productoSelected != null) {
            if (userSessionBeanLocal.cantidadProducto(relFacturaProductos, productoSelected) > 0) {
                relFacturaProductos.add(new RelFacturaProducto(factura, productoSelected));
                listFieldProductosSeleccionados.setListData(
                        userSessionBeanLocal.listaLabelsRelFacturaProducto(relFacturaProductos));
                labelTotal.setText(userSessionBeanLocal.total(relFacturaProductos));
            } else {
                JOptionPane.showMessageDialog(null, "No hay mas de este producto disponible.");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Debe seleccionar un producto.");
        }
    }//GEN-LAST:event_buttonAgregarProductoActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        if (relFacturaProductoSelected != null) {
            relFacturaProductos.remove(relFacturaProductoSelected);
            listFieldProductosSeleccionados.setListData(
                    userSessionBeanLocal.listaLabelsRelFacturaProducto(relFacturaProductos));
            labelTotal.setText(userSessionBeanLocal.total(relFacturaProductos));
        } else {
            JOptionPane.showMessageDialog(null, "Seleccione un producto.");
        }
    }//GEN-LAST:event_jButton4ActionPerformed

